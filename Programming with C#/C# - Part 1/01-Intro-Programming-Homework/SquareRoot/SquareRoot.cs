using System;

// Task Description:
// Write a program that calculates the square root of the number 12345 and prints it on the console.

namespace SquareRoot
{
    class SquareRoot
    {
        static void Main()
        {
            int number = 12345;
            double squareNumber = Math.Sqrt(number);
            Console.WriteLine(squareNumber);
        }
    }
}
